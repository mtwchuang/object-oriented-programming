package Week_09;
public class Arithmetic 
{
    public static int multiply(int a, int b)
    {
        return (a*b);
    }
    public static double divide(double a, double b)
    {
        return (a/b);
    }
    public static boolean isPositive(int n)
    {
        return ((n<0)? false:true);
    }
}
